                        <strong>一、工程<br />
                        </strong><span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;"></span><br />
                        <span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">一、工程简介</span><br />
                        <br />
                        <span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">河南万邦水果保鲜冷库由河南荣兴制冷设备有限公司提供设备及安装.经过我公司的现场勘查给出安装建议,客户采纳后.我公司快速备货及安装.前后一周的时间.关于此冷库由于是芒果冷库和香蕉冷库,我公司配以风冷冷凝器,鉴于客户的场地比较大,我公司配15p压缩机组,已达到快速降温的目的.能够长久的保持冷库达到相对的温度,防止水果的腐烂.&nbsp;</span><br />
                        <br />
                        <span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">常见水果冷库温湿度</span><br />
                        <span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">苹果：温度0℃～+1℃，湿度85%～88%</span><br />
                        <span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">梨子：温度0.3～0.8℃，湿度82%～86%</span><br />
                        <span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">香蕉：温度6.2℃～8.4℃，湿度75%～80%</span><br />
                        <span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">柑桔：温度1.5℃～4.3℃，湿度74%～76%</span><br />
                        <span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">桃、李最佳保存温度为0～0.2℃，湿度78%～86%</span><br />
                        <span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">批把：温度0.1℃~0.4℃</span><br />
                        <span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">葡萄：温度0.1℃~0.2℃，湿度82%~86%。&nbsp;</span><br />
                        <br />
                        <strong>二、冷库用材料及主要设备<br />
                        </strong><span style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;"></span><br />
                        <p style="color:#333333;font-family:&quot;font-size:14px;background-color:#FFFFFF;">
                            项目介绍：<br />
                            一、冷库介绍<br />
                            1、尺寸：8米×9米×2.6米<br />
                            2、温度：6.2℃～8.4℃；<br />
                            3、冷库类型：保鲜冷库<br />
                            4、储存产品：香蕉,芒果等<br />
                            5、库门：手动推拉门<br />
                            6、库体结构：拼装库体<br />
                            7、库板：不锈钢库板聚氨酯材料、厚度10cm<br />
                            8、电控系统：半自动控制<br />
                            二、制冷设备：<br />
                            1、压缩机：谷轮15匹 压缩机组<br />
                            2、压缩机功率：（11.25kw）<br />
                            3、压缩机类别：（半封闭）<br />
                            4、冷却方式：风冷却<br />
                            三、电气控制<br />
                            1、冷库系统供电标准：3相交流电，使用电压380±5%，控制电压220V/50HZ。<br />
                            2、温度显示方式：机房集中显示<br />
                            3、总体控制要求：采用机房库房集中分散相结合的控制方式<br />
                            4、库房保护设置：当有人误锁库内，可按门旁呼救按钮，在控制室声、光报警。<br />
                            5、压缩机操作方式：手动（PLC控制）<br />
                            四、配件<br />
                            1、阀门：爱姆基<br />
                            2、链接管：10m无缝钢管<br />
                            3、冷媒（制冷剂）（134a）<br />
                            4、照明灯：防潮防爆<br />
                            5、辅助电柜：施耐德<br />
                            6、压力控制器：PK-11（丹佛斯）<br />
                            7、压力表：-0.1～2.5Mpa（大冷）<br />
                            8、管道：主管道采用GB8163-1999流体无缝钢管，部分管采用YB447-70紫铜管。
                        </p>
